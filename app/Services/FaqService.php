<?php

namespace App\Services;

/**
 * Простий сервіс FAQ:
 * - тримає кілька поширених запитань/відповідей;
 * - вміє по тексту користувача знайти релевантну відповідь.
 *
 * Зараз усе захардкожено в масиві.
 * Пізніше можна винести в БД / адмінку.
 */
class FaqService
{
    /**
     * Масив часто заданих питань.
     *
     * keys  — варіанти формулювань, які ми ловимо в повідомленні користувача
     * answer — текст відповіді, яку віддаємо в API
     */
    protected array $faqs = [
        [
            'keys'   => [
                'як оплатити',
                'як оплатити замовлення',
                'спосіб оплати',
                'способи оплати',
                'оплата',
                'оплатити',
                'оплатить',
                'оплатеж',
                'как оплатить',
            ],
            'answer' => 'Оплатити замовлення можна онлайн банківською карткою або при отриманні (накладений платіж), якщо цей спосіб доступний на сторінці оформлення замовлення.',
        ],
        [
            'keys'   => [
                'доставка',
                'як доставляєте',
                'як ви доставляєте',
                'скільки коштує доставка',
                'вартість доставки',
                'умови доставки',
                'спосіб доставки',
                'служба доставки',
            ],
            'answer' => 'Доставка здійснюється службами (наприклад: Нова пошта / Укрпошта / самовивіз — залежно від налаштувань магазину). Актуальні варіанти та вартість доставки завжди видно на сторінці оформлення замовлення.',
        ],
        [
            'keys'   => [
                'повернення',
                'обмін',
                'як повернути',
                'як зробити повернення',
                'умови повернення',
                'гарантія',
            ],
            'answer' => 'Товар можна повернути або обміняти відповідно до Закону України «Про захист прав споживачів». Детальні умови повернення та гарантії описані на сторінці з правилами магазину (публічна оферта / умови повернення).',
        ],
    ];

    /**
     * Головний метод: пробує знайти відповідь на запит користувача.
     * Повертає ТІЛЬКИ текст відповіді або null, якщо нічого не знайшли.
     */
    public function findAnswer(string $message): ?string
    {
        $normalized = $this->normalize($message);

        foreach ($this->faqs as $faq) {
            if ($this->containsAny($normalized, $faq['keys'])) {
                return $faq['answer'];
            }
        }

        return null;
    }

    /**
     * Метод для нового коду: повертає НЕ тільки текст, а й трішки структури.
     * Якщо нічого не знайшли — повертає null.
     *
     * Це той метод, через відсутність якого в тебе зараз летить 500.
     */
    public function match(string $message): ?array
    {
        $normalized = $this->normalize($message);

        foreach ($this->faqs as $faq) {
            if ($this->containsAny($normalized, $faq['keys'])) {
                return [
                    'question_stub' => $faq['keys'][0], // умовне "ключове" питання
                    'answer'        => $faq['answer'],
                ];
            }
        }

        return null;
    }

    /**
     * Нормалізація текста:
     * - до нижнього регістру
     * - обрізаємо пробіли
     * - стискаємо кілька пробілів в один
     */
    protected function normalize(string $text): string
    {
        $text = mb_strtolower(trim($text), 'UTF-8');
        $text = preg_replace('/\s+/', ' ', $text);

        return $text ?? '';
    }

    /**
     * Перевіряємо, чи містить текст хоч один із ключів.
     */
    protected function containsAny(string $haystack, array $needles): bool
    {
        foreach ($needles as $needle) {
            $needle = mb_strtolower($needle, 'UTF-8');
            if ($needle !== '' && mb_stripos($haystack, $needle, 0, 'UTF-8') !== false) {
                return true;
            }
        }

        return false;
    }
}
