<?php

namespace App\Services;

/**
 * Дуже простий FAQ-сервіс.
 *
 * Зараз це просто заглушка: кілька хардкодних відповідей.
 * Пізніше можна винести FAQ в базу даних або окремий YAML/JSON.
 */
class FaqService
{
    /**
     * Спробувати знайти відповідь на питання користувача.
     *
     * @param  string  $message  Повідомлення користувача
     * @return string|null       Текст відповіді або null, якщо нічого не знайшли
     */
    public function findAnswer(string $message): ?string
    {
        $text = mb_strtolower($message);

        // 👇 Прості приклади. Можеш змінити під свій магазин
        if ($this->containsAny($text, ['доставка', 'скільки йде', 'як довго йде', 'коли приїде'])) {
            return 'Доставка по Україні займає в середньому 1–3 робочих дні, залежно від перевізника і міста. Уточнити точні терміни можна на сторінці оформлення замовлення.';
        }

        if ($this->containsAny($text, ['оплата', 'як оплатити', 'чи можна накладеним', 'наложка'])) {
            return 'Оплатити замовлення можна онлайн банківською карткою або при отриманні (накладений платіж), якщо цей спосіб доступний на сторінці оформлення замовлення.';
        }

        if ($this->containsAny($text, ['повернення', 'обмін', 'можна повернути'])) {
            return 'Так, ви можете оформити повернення або обмін товару протягом 14 днів за умови збереження товарного вигляду і чеку. Деталі — в розділі «Умови повернення» на сайті.';
        }

        // Якщо нічого не знайшли — повертаємо null, тоді ChatController піде в пошук товарів
        return null;
    }

    /**
     * Допоміжна функція: чи містить рядок хоч одне зі слів.
     */
    protected function containsAny(string $haystack, array $needles): bool
    {
        foreach ($needles as $needle) {
            if (mb_strpos($haystack, $needle) !== false) {
                return true;
            }
        }

        return false;
    }
}
